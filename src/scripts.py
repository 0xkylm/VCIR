import random


def WriteOnRegStr(str):
    i = 0
    b = len(str)
    groups = []  # Crée une liste pour stocker les groupes de trois nombres hexadécimaux

    for char in str:
        group = f"0x14,{hex(ord(char))},{hex(i)}"
        groups.append(group)
        i += 1

    random.shuffle(groups)  # Mélange les groupes de manière aléatoire

    for group in groups:
        print(group, end=",")


